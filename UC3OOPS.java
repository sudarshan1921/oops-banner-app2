/** UC3 - Printing OOPS using Sting.join()*/
public class UC3OOPS {
    public static void main(String[] args) {

        System.out.println(String.join("  ", " ***** ", " ***** ", " ***** ",  "****** "));
        System.out.println(String.join("  ", "*     *", "*     *", "*     *",  "*      "));
        System.out.println(String.join("  ", "*     *", "*     *", "*     *",  "*      "));
        System.out.println(String.join("  ", "*     *", "*     *", " ***** ",  "*****  "));
        System.out.println(String.join("  ", "*     *", "*     *", "*       ", "    * "));
        System.out.println(String.join("  ", "*     *", "*     *", "*       ", "    * "));
        System.out.println(String.join("  ", " ***** ", " ***** ", "*       ", "*****"));

    }
}
