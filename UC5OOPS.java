/* UC5 - Array init */
public class UC5OOPS {

    public static void main(String[] args) {

        String[] lines = {
            String.join(" ", " *** ", " *** ", " ***** ", " ***** "),
            String.join(" ", "*   *", "*   *", "*     ", "*     "),
            String.join(" ", "*   *", "*   *", "***** ", "***** "),
            String.join(" ", "*   *", "*   *", "*     ", "    * "),
            String.join(" ", " *** ", " *** ", " *****", "***** ")
        };

        for (String line : lines) {
            System.out.println(line);
        }
    }
}